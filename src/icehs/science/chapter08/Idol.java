package icehs.science.chapter08;

public class Idol {

	private String name;
	private String group;
	
	public Idol(String group, String name) {
		this.group = group;
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public String getGroup() {
		return group;
	}
	
	
	
	
	
}
